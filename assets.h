/*
 * Color encoding for tiles:
 *
 * - Each pixel is a 4-bit palette index 0–15.
 * - These indices map to the enum in vga16_graphics_v2.h:
 *
 *   0  BLACK
 *   1  DARK_GREEN
 *   2  MED_GREEN
 *   3  GREEN
 *   4  DARK_BLUE
 *   5  BLUE
 *   6  LIGHT_BLUE
 *   7  CYAN
 *   8  RED
 *   9  DARK_ORANGE
 *   10 ORANGE
 *   11 YELLOW
 *   12 MAGENTA
 *   13 PINK
 *   14 LIGHT_PINK
 *   15 WHITE
 *
 * Our Flappy Bird sprite only uses:
 *   0x0 (BLACK background/outline),
 *   0xA (ORANGE beak),
 *   0xB (YELLOW body),
 *   0xF (WHITE eye/wing).
 */

#define COLOR_BLACK   0x0
#define COLOR_ORANGE  0xA
#define COLOR_YELLOW  0xB
#define COLOR_WHITE   0xF

